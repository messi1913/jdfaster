package com.jdfaster.view.data;

public class ScTestInfo {
	
	private String name;
	private String url;
	private String className;
	private String methodName;
	private String conTimeOut;
	private String ResTitmeOut;
	private String threadSize;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getMethodName() {
		return methodName;
	}
	public void setMethodName(String methodName) {
		this.methodName = methodName;
	}
	public String getConTimeOut() {
		return conTimeOut;
	}
	public void setConTimeOut(String conTimeOut) {
		this.conTimeOut = conTimeOut;
	}
	public String getResTitmeOut() {
		return ResTitmeOut;
	}
	public void setResTitmeOut(String resTitmeOut) {
		ResTitmeOut = resTitmeOut;
	}
	public String getThreadSize() {
		return threadSize;
	}
	public void setThreadSize(String threadSize) {
		this.threadSize = threadSize;
	}
	
	

}
